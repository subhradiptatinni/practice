public class pattern2 {

	public static void main(String[] args) {
		
		System.out.println (" *");
		for(int i=0;i<=4;i+=2)
		{
			for (int j=0;j<=i+2;j++)
			{
			
				System.out.print(" *");
			
			}
			System.out.println();

		}
		

	}

}
